    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        ArrayList<TableBookings> allTableBookings = MySQLUtil.getAllTableBookings();
        
        for(TableBookings m : allTableBookings) {
            Object[] row = new Object[6];
            row[0] = m.getId();
            row[1] = m.getTable_no();
            row[2] = m.getRestrauntId();
            row[3] = m.getRestraunt();
            row[4] = m.getStatus();
            row[5] = m.getFrom();
            
            model.addRow(row);
        }
    }    
    
    private void getUser() {
        ArrayList<Person> allPerson = MySQLUtil.getAllPersons();
        String[] username = new String[allPerson.size()];
        for(int i=0; i<allPerson.size(); i++) {
            username[i] = allPerson.get(i).getFirstname();
        }
        
        getUser.setModel(new javax.swing.DefaultComboBoxModel<>(username));
//        JComboBox levelComboBox = (JComboBox)evt.getSource();
        
        
        
    }

