    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        ArrayList<Rooms> allRooms = MySQLUtil.getAllRooms();
        
        for(Rooms h : allRooms) {
            Object[] row = new Object[7];
            row[0] = h.getRoom_no();
            row[1] = h.getHotelId();
            row[2] = h.getHotel();
            row[3] = h.getType();
            row[4] = h.getDesc();
            row[5] = h.getPrice();
            row[6] = h.getStatus();
            model.addRow(row);
        }
    }

