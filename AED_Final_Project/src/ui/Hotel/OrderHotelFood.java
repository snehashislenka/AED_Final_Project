     private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        ArrayList<Hotel> allHotel = MySQLUtil.getAllHotel();
        
        for(Hotel h : allHotel) {
            Object[] row = new Object[5];
            row[0] = h.getHotel();
            row[1] = h.getAddress();
            row[2] = h.getCity();
            row[3] = h.getZipcode();
            row[4] = h.getId();
            
            model.addRow(row);
        }
    }    

