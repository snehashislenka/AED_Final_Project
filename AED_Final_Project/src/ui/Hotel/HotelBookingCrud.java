    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        
        ArrayList<HotelBookings> allBookings = MySQLUtil.getHotelBookings();
        
        for(HotelBookings b : allBookings) {
            Object[] row = new Object[8];
            row[0] = b.getRoom_no();
            row[1] = b.getUser();
            row[2] = b.getFrom();
            row[3] = b.getTo();
            row[4] = b.getPrice();
            row[5] = b.getStatus();
            row[6] = b.getHotelId();
            row[7] = b.getId();
            
            model.addRow(row);
        }
    }    
    
    private void populateRoomNoTable(int hotelId, int room_no) {
        if(room_no > 0) {
            JOptionPane.showMessageDialog(this,"Room already assigned!");
            return;
        } 
        System.out.println("here------------"+hotelId);
        ArrayList<Rooms> roomsList = new ArrayList(); 
        String query = "Select r.room_no from rooms r inner join hotel_bookings h on r.hotelId = h.hotelId where r.hotelId = ? and (h.status = 'CHECKED-OUT' or r.status = 'AVAILABLE') group by r.room_no";
         try {
            Connection conn = connectMySQL();
            PreparedStatement ps = conn.prepareStatement(query); 
            
            ps.setInt(1,hotelId);
            
            ResultSet rs = ps.executeQuery();   
            
//        System.out.println("here------------"+rs.getInt("room_no"));
            while(rs.next()) {
                Rooms res = new Rooms(rs.getInt("room_no"));
                roomsList.add(res);
            }
        } catch (SQLException ex) {
            Logger.getLogger(MySQLUtil.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        for(Rooms r : roomsList) {
            Object[] row = new Object[6];
            row[0] = r.getRoom_no();
            
            model.addRow(row);
        }
    }
    
