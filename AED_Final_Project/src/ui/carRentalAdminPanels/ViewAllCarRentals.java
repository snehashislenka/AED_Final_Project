    public void populateTable() {
        ArrayList<CarRental> carRentalList = new 
            ArrayList<>();
        
        carRentalList = MySQLUtil.getAllCarRentalDetails();
        
        DefaultTableModel model = (DefaultTableModel) tableCarRentals.getModel();
        model.setRowCount(0);
        for(CarRental carRental : carRentalList) {
            Object[] row = new Object[9];
            row[0] = carRental.getRentalId();
            row[1] = carRental.getPickupLocation();
            row[2] = carRental.getRatePerHour();
            row[3] = carRental.getMileage();
            row[4] = carRental.getFuelType();
            row[5] = carRental.getSeats();
            row[6] = carRental.getCarType();
            row[7] = carRental.getCarCompany();
            row[8] = carRental.getCarModel();
            
            model.addRow(row);
        }
    }

