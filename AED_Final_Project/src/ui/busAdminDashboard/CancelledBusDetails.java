    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tableCanceledBus.getModel();
        model.setRowCount(0);
        
        String query = "select b.busId id, b.busCompanyName company, b.departureBusStation departStn, b.arrivalBusStation arrStn,\n" +
" b.departingTimestamp depTime, b.arrivalTimestamp arrTime from bus_bookings bb inner join bus b on bb.busId = b.busId where booking_status='CANCELED'";
        
        try {
            Connection conn = MySQLUtil.connectMySQL();
            
            PreparedStatement ps = conn.prepareStatement(query);
            
            ResultSet rs = ps.executeQuery();
            
            while(rs.next()) {
                Object[] row = new Object[6];
                row[0] = rs.getInt("id");
                row[1] = rs.getString("company");
                row[2] = rs.getString("departStn");
                row[3] = rs.getString("arrStn");
                row[4] = rs.getString("depTime");
                row[5] = rs.getString("arrTime");
                
                model.addRow(row);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        
    }
    
