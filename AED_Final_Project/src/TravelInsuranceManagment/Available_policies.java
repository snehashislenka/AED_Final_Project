    private void ViewPolicyTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ViewPolicyTableMouseClicked
        // TODO add your handling code here:
        int selectedRow = ViewPolicyTable.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to update!");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) ViewPolicyTable.getModel();
        int selectedpolicyId = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());
        
        Policy selectedPolicy = MySQLUtil.getPolicyById(selectedpolicyId);
        
        PolicyIDLabel.setText(String.valueOf(selectedPolicy.getPolicyID()));
        PolicyNameLabel.setText(selectedPolicy.getPolicyName());
        PolicyCategoryLabel.setText(selectedPolicy.getCategoryName());
        PolicySumassuranceLabel.setText(String.valueOf(selectedPolicy.getPolicySumAssurance()));
        PolicyPremiumLabel.setText(String.valueOf(selectedPolicy.getPolicyPremium()));
        PolicyTenureLabel.setText(String.valueOf(selectedPolicy.getPolicyTenure()));
    }//GEN-LAST:event_ViewPolicyTableMouseClicked

    private void ApplybtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ApplybtnActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = ViewPolicyTable.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to update!");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) ViewPolicyTable.getModel();
        int selectedpolicyId = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());
        
        int personId = MySQLUtil.getPersonSession().getId();
        
        Date date = new Date();
        System.out.println(date.toString());
        String AppliedDate = date.toString();
        String Status = "Applied";
        MySQLUtil.addpolicybookingdetails(selectedpolicyId, personId, AppliedDate,Status);
        
        JOptionPane.showMessageDialog(this,"Congratulations! Apply for policy sucessfully");
        
    }//GEN-LAST:event_ApplybtnActionPerformed


