    private void SerachbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SerachbtnActionPerformed
        // TODO add your handling code here:
        int CategoryID  =  Integer.parseInt(CategoryIDTextField.getText());
        String query = "SELECT * FROM insurance_category where id = '"+ CategoryID +"'";
         try {          
            Connection conn = connectMySQL();
            PreparedStatement ps = conn.prepareStatement(query); 
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                CategoryNameTextField.setText(rs.getString("categoryname"));
                CategoryDateTextField.setText(rs.getString("categorydate"));
            }  
            
        } catch (SQLException ex) {
            Logger.getLogger(MySQLUtil.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_SerachbtnActionPerformed

